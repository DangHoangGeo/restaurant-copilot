        lastAutoPrintResult = "🔄 Print history cleared"
        print("Print history and statistics cleared")
        
        // Clear the result message after 3 seconds
        Task {
            try? await Task.sleep(nanoseconds: 3_000_000_000)
            lastAutoPrintResult = nil
        }
    }
    
    // MARK: - Helper Functions
    // Ensure TimeoutError is defined if not already present, or use a general error.
    // struct TimeoutError: LocalizedError { ... } was present at the end of the original file.
    private func withTimeout<T>(seconds: TimeInterval, operation: @escaping () async throws -> T) async throws -> T {
        return try await withThrowingTaskGroup(of: T.self) { group in
            group.addTask {
                return try await operation()
            }
            
            group.addTask {
                try await Task.sleep(nanoseconds: UInt64(seconds * 1_000_000_000))
                throw TimeoutError()
            }
            
            guard let result = try await group.next() else {
                throw TimeoutError()
            }
            
            group.cancelAll()
            return result
        }
    }
