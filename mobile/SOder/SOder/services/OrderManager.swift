        }
    }
    
    // MARK: - Realtime Subscription Management
    private func setupRealtimeSubscription() async {
        // Clean up any existing subscription first
        await cleanupRealtimeSubscription()
        
        guard supabaseManager.isAuthenticated else {
            print("Cannot setup realtime subscription - user not authenticated")
            return
        }
        
        guard let restaurantId = supabaseManager.currentRestaurantId else {
            print("Cannot setup realtime subscription - no restaurant ID")
            return
        }
        
        print("Setting up realtime subscription for restaurant: \(restaurantId)")
        
        realtimeChannel = supabaseManager.client.realtimeV2.channel("orders-\(restaurantId)")
        
        // Listen for order insertions
        if let ordersInsertion = realtimeChannel?.postgresChange(
            InsertAction.self,
            schema: "public",
            table: "orders",
            filter: "restaurant_id=eq.\(restaurantId)"
        ) {
            Task { [weak self] in
                for await _ in ordersInsertion {
                    print("New order inserted via realtime")
                    await MainActor.run {
                        Task { @MainActor in
                            await self?.handleOrderChange()
                        }
                    }
                }
            }
        }
        // Listen for order updates
        if let ordersUpdate = realtimeChannel?.postgresChange(
            UpdateAction.self,
            schema: "public",
            table: "orders",
            filter: "restaurant_id=eq.\(restaurantId)"
        ) {
            Task { [weak self] in
                for await _ in ordersUpdate {
                    print("Order updated via realtime")
                    await MainActor.run {
                        Task { @MainActor in
                            await self?.handleOrderChange()
                        }
                    }
                }
            }
        }
        // Listen for order item updates
        if let orderItemsUpdate = realtimeChannel?.postgresChange(
            UpdateAction.self,
            schema: "public",
            table: "order_items"
        ) {
            Task { [weak self] in
                for await _ in orderItemsUpdate {
                    print("Order item updated via realtime")
                    await MainActor.run {
                        Task { @MainActor in
                            await self?.handleOrderItemChange()
                        }
                    }
                }
            }
        }
        // Listen for subscription status
        if let channel = realtimeChannel {
            Task { [weak self] in
                await channel.subscribe()
                await MainActor.run {
                    self?.isRealtimeConnected = true
                }
            }
        }
    }
    
    private func cleanupRealtimeSubscription() async {
        if let channel = realtimeChannel {
            await channel.unsubscribe()
            realtimeChannel = nil
            print("Cleaned up realtime subscription")
        }
        await MainActor.run {
            isRealtimeConnected = false
        }
    }
    
    @MainActor
    private func handleOrderChange() async {
        let previousOrders = orders
        print("📋 Order change detected: previous=\(previousOrders.count), fetching fresh data...")
        await fetchActiveOrders()
        print("📋 After fetch: current=\(orders.count)")
        
        let canAutoPrint = hasKitchenPrinter()
        
        // Check for new orders to auto-print
        if orders.count > previousOrders.count {
            triggerFeedback()
            
            let newOrderCount = orders.count - previousOrders.count
            print("🆕 Detected \(newOrderCount) new order(s)")
            print("🖨️ Auto-print check: enabled=\(autoPrintingEnabled), hasKitchenPrinter=\(canAutoPrint)")
            
            // Auto-print new orders if enabled and kitchen printer is configured
            if autoPrintingEnabled && canAutoPrint {
                print("🖨️ Triggering auto-print for new orders")
                await autoPrintNewOrders(previousOrders: previousOrders)
            } else {
                print("🖨️ Auto-print skipped - enabled: \(autoPrintingEnabled), canPrint: \(canAutoPrint)")
            }
        } else if orders.count == previousOrders.count {
            print("📋 No new orders detected (same count), checking for new items...")
            
            // Check for new items added to existing orders
            let newItems = findNewItemsInExistingOrders(currentOrders: orders, previousOrders: previousOrders)
            
            if !newItems.isEmpty {
                print("🍽️ Detected \(newItems.count) new item(s) in existing orders")
                triggerFeedback()
                
                if autoPrintingEnabled && canAutoPrint {
                    print("🖨️ Triggering auto-print for new items")
                    await autoPrintNewItems(newItems)
                } else {
                    print("🖨️ Auto-print skipped for items - enabled: \(autoPrintingEnabled), canPrint: \(canAutoPrint)")
                }
            } else {
                print("📋 No new items detected")
            }
        } else {
            print("📋 Order count decreased (completed/canceled orders?)")
        }
    }
    
