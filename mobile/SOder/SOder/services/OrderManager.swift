    @MainActor
    public func startNewOrder(tableId: String, guestCount: Int?) async throws -> Order {
        guard let restaurantId = supabaseManager.currentRestaurantId else {
            throw OrderManagerError.missingRestaurantId
        }
        guard let userId = supabaseManager.currentUser?.id.uuidString else {
            // In a real app, user ID should always be available after login.
            // This might indicate an issue with session management or user data fetching.
            throw OrderManagerError.missingUserId
        }

        let newOrderPayload = NewOrderPayload(
            restaurant_id: restaurantId,
            table_id: tableId,
            user_id: userId, // Assuming current authenticated user is placing the order
            guest_count: guestCount,
            status: ORDER_STATUS_DRAFT
        )

        do {
            let createdOrder: Order = try await supabaseManager.client
                .from("orders")
                .insert(newOrderPayload, returning: .representation)
                .select("*, table:tables(*), order_items(*, menu_item:menu_items(*))") // Fetch related data
                .single()
                .execute()
                .value

            self.currentDraftOrder = createdOrder
            print("Draft order started: \(createdOrder.id)")
            return createdOrder
        } catch {
            print("Error starting new order: \(error)")
            throw OrderManagerError.generalError("Failed to start new order: \(error.localizedDescription)")
        }
    }

    @MainActor
    public func addItemToDraftOrder(orderId: String, menuItemId: String, quantity: Int, notes: String?, selectedSizeId: MenuItemSizeId?, selectedToppingIds: [ToppingId]?, priceAtOrder: Double) async throws -> OrderItem {

        guard let restaurantId = supabaseManager.currentRestaurantId else {
            throw OrderManagerError.missingRestaurantId
        }

        // Price calculation is now done in AddItemDetailView and passed as priceAtOrder.
        // The MOCK PRICE CALCULATION block has been removed.

        let newOrderItemPayload = NewOrderItemPayload(
            order_id: orderId,
            menu_item_id: menuItemId,
            quantity: quantity,
            notes: notes,
            restaurant_id: restaurantId,
            price_at_order: priceAtOrder, // Use the price calculated and passed by the view
            status: ORDER_ITEM_STATUS_DRAFT,
            menu_item_size_id: selectedSizeId,
            topping_ids: selectedToppingIds
        )

        do {
            let createdOrderItem: OrderItem = try await supabaseManager.client
                .from("order_items")
                .insert(newOrderItemPayload, returning: .representation)
                .select("*, menu_item:menu_items(*), menu_item_size:menu_item_sizes(*)") // Fetch related data
                .single()
                .execute()
                .value

            // Append to currentDraftOrder
            if currentDraftOrder?.id == orderId {
                currentDraftOrder?.order_items?.append(createdOrderItem)
                // Recalculate total price for the draft order
                recalculateCurrentDraftOrderTotal()
            } else {
                // If currentDraftOrder is not set or is different, fetch it to ensure consistency
                _ = try await getDraftOrder(orderId: orderId)
                currentDraftOrder?.order_items?.append(createdOrderItem)
                recalculateCurrentDraftOrderTotal()
            }

            print("Item added to draft order: \(createdOrderItem.id)")
            return createdOrderItem
        } catch {
            print("Error adding item to draft order: \(error)")
            throw OrderManagerError.generalError("Failed to add item: \(error.localizedDescription)")
        }
    }

    @MainActor
    private func recalculateCurrentDraftOrderTotal() {
        guard var draftOrder = currentDraftOrder, let items = draftOrder.order_items else { return }

        let total = items.reduce(0.0) { acc, item in
            acc + (item.price_at_order * Double(item.quantity))
        }
        draftOrder.total_amount = total
        currentDraftOrder = draftOrder

        // Optionally, update the order total in the database immediately for draft orders
        // This could be useful if another device needs to see the running total.
        // However, it might be better to do this only when confirming the order or explicitly saving a draft.
        // For now, we'll keep the total local until confirm/save.
    }


    @MainActor
    public func updateDraftOrderItem(orderItemId: String, newQuantity: Int, newNotes: String?) async throws -> OrderItem {
        // Note: This simplified version doesn't recalculate price_at_order if options change.
        // A full implementation would need to handle potential changes to selectedSize/selectedToppings
        // and refetch/recalculate price_at_order if they impact it.

        let updatePayload = UpdateOrderItemPayload(
            quantity: newQuantity,
            notes: newNotes
        )

        do {
            let updatedOrderItem: OrderItem = try await supabaseManager.client
                .from("order_items")
                .update(updatePayload)
                .eq("id", value: orderItemId)
                .select("*, menu_item:menu_items(*), menu_item_size:menu_item_sizes(*)")
                .single()
                .execute()
                .value

            // Update in currentDraftOrder
            if let index = currentDraftOrder?.order_items?.firstIndex(where: { $0.id == orderItemId }) {
                currentDraftOrder?.order_items?[index] = updatedOrderItem
                recalculateCurrentDraftOrderTotal()
            }
            print("Draft order item updated: \(updatedOrderItem.id)")
            return updatedOrderItem
        } catch {
            print("Error updating draft order item: \(error)")
            throw OrderManagerError.orderItemUpdateFailed(orderItemId)
        }
    }

    @MainActor
    public func removeDraftOrderItem(orderItemId: String) async throws {
        do {
            try await supabaseManager.client
                .from("order_items")
                .delete()
                .eq("id", value: orderItemId)
                .execute()

            // Remove from currentDraftOrder
            currentDraftOrder?.order_items?.removeAll(where: { $0.id == orderItemId })
            recalculateCurrentDraftOrderTotal()
            print("Draft order item removed: \(orderItemId)")
        } catch {
            print("Error removing draft order item: \(error)")
            throw OrderManagerError.generalError("Failed to remove item: \(error.localizedDescription)")
        }
    }

    @MainActor
    public func clearDraftOrder(orderId: String) async throws {
        // Ensure this order is the current draft before clearing locally
        if currentDraftOrder?.id != orderId {
             _ = try? await getDraftOrder(orderId: orderId) // Load it if not current
        }

        do {
            // 1. Delete all order_items associated with the orderId
            try await supabaseManager.client
                .from("order_items")
                .delete()
                .eq("order_id", value: orderId)
                .execute()
            print("Deleted items for draft order: \(orderId)")

            // 2. Delete the order itself
            try await supabaseManager.client
                .from("orders")
                .delete()
                .eq("id", value: orderId)
                .execute()
            print("Deleted draft order: \(orderId)")

            // Clear local state if it was the current draft
            if currentDraftOrder?.id == orderId {
                currentDraftOrder = nil
            }
        } catch {
            print("Error clearing draft order: \(error)")
            throw OrderManagerError.generalError("Failed to clear draft order: \(error.localizedDescription)")
        }
    }

    @MainActor
    public func confirmOrderToKitchen(orderId: String) async throws -> Order {
        var orderToConfirm: Order
        
        if let currentOrder = currentDraftOrder, currentOrder.id == orderId {
            orderToConfirm = currentOrder
        } else {
            // Fetch the order from database
            if let fetchedOrder = try await getDraftOrder(orderId: orderId, forceFetch: true) {
                orderToConfirm = fetchedOrder
            } else {
                throw OrderManagerError.orderUpdateFailed("Order \(orderId) not found for confirmation.")
            }
        }

        // Recalculate total price one last time based on current items, just in case.
        let finalTotalPrice = orderToConfirm.order_items?.reduce(0.0) { $0 + ($1.price_at_order * Double($1.quantity)) } ?? 0

        // Create a proper Codable struct for the update payload
        struct OrderUpdatePayload: Codable {
            let status: String
            let total_amount: Double
            let updated_at: String
        }
        
        let updatePayload = OrderUpdatePayload(
            status: "new", // Change status to "new" or similar for kitchen
            total_amount: finalTotalPrice,
            updated_at: ISO8601DateFormatter().string(from: Date())
        )

        do {
            let confirmedOrder: Order = try await supabaseManager.client
                .from("orders")
                .update(updatePayload)
                .eq("id", value: orderId)
                .select("*, table:tables(*), order_items(*, menu_item:menu_items(*), menu_item_size:menu_item_sizes(*))")
                .single()
                .execute()
                .value

            // Clear the local draft order state as it's no longer a draft
            currentDraftOrder = nil

            // Optionally, add to the main `orders` list if it's now an active order
            // Or rely on real-time updates / next fetchActiveOrders to pick it up.
            // For immediate UI update, consider adding it:
            if !self.orders.contains(where: { $0.id == confirmedOrder.id }) {
                 self.orders.insert(confirmedOrder, at: 0) // Add to top
            }

            print("Order confirmed to kitchen: \(confirmedOrder.id)")
            return confirmedOrder
        } catch {
            print("Error confirming order to kitchen: \(error)")
            throw OrderManagerError.orderUpdateFailed(orderId)
        }
    }

