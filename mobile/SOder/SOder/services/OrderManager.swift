    @MainActor
    public func cancelOrder(orderId: String) async throws {
        guard let restaurantId = supabaseManager.currentRestaurantId else {
            throw OrderManagerError.missingRestaurantId
        }
        
        // Check if order exists and can be canceled
        let order: Order = try await supabaseManager.client
            .from("orders")
            .select("id, status")
            .eq("id", value: orderId)
            .eq("restaurant_id", value: restaurantId)
            .single()
            .execute()
            .value
        
        guard order.status == .new || order.status == .draft || order.status == .serving else {
            throw OrderManagerError.generalError(String(format: "error_cancel_order_status".localized, order.status.displayName))
        }
        
        // Update order status to canceled
        try await supabaseManager.client
            .from("orders")
            .update(["status": "canceled"])
            .eq("id", value: orderId)
            .execute()
        
        // Update all order items to canceled  
        try await supabaseManager.client
            .from("order_items")
            .update(["status": "canceled"])
            .eq("order_id", value: orderId)
            .execute()
        
        print("✅ canceled order: \(orderId)")
        
        // Refresh orders list
        await fetchActiveOrders()
    }

